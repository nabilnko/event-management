package com.example.eventmanagement.dto;

import io.swagger.v3.oas.annotations.media.Schema;

public class LoginResponseDTO {

    @Schema(description = "JWT authentication token", example = "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...")
    private String token;

    @Schema(description = "Token type", example = "Bearer")
    private String type = "Bearer";

    @Schema(description = "Username of authenticated user", example = "john_superadmin")
    private String username;

    @Schema(description = "Role of authenticated user", example = "SUPER_ADMIN")
    private String role;

    @Schema(description = "Token expiration time in milliseconds", example = "86400000")
    private Long expiresIn;

    // Constructors
    public LoginResponseDTO() {
    }

    public LoginResponseDTO(String token, String username, String role, Long expiresIn) {
        this.token = token;
        this.username = username;
        this.role = role;
        this.expiresIn = expiresIn;
    }

    // Getters and Setters
    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public Long getExpiresIn() {
        return expiresIn;
    }

    public void setExpiresIn(Long expiresIn) {
        this.expiresIn = expiresIn;
    }
}
